			<p>Главную роль в четвертом сезоне играет Крис Рок, это уже ясно, а вот дата премьеры все еще плавает и, возможно,
				вообще переползет на 2020 год.</p>

			<h4>«Рик и Морти»</h4>
			<p>Легендарный сериал и его не менее легендарные разрывы между сезонами. Четвертый сезон должен выйти под конец
				2019-го, ждать еще долго, но мы знаем, что оно того стоит.</p>

			<h4>«Охотник за разумом»</h4>
			<p>Потрясающий телетриллер от Дэвида Финчера, заставляющего сериалы выйти на киноуровень, точно получит второй сезон
				в 2019-м, но ни намека на конкретную дату пока нет.</p>

			<h4>«Готэм»</h4>
			<p>Пятый сезон, выходящий уже третьего января, станет последним. Иронично, что шоу, которое я изначально
				раскритиковал, стало единственным супергеройским сериалом вне линейки Netflix/Marvel, который я все еще смотрю.
				Жаль будет расставаться с этим живописно разросшимся миром Бэтмена.</p>

			<h4>«Агенты Щ.И.Т»</h4>
			<p>У Агентов же летом выходит уже шестой сезон, и он последним не станет, седьмой подтвержден уже заранее.</p>

			<h4>«Элементарно»</h4>
			<p>Американская версия шоу про современного Холмса в Нью-Йорке решила сменить обстановку и вернуть своего Шерлока на
				родину — в Лондон. Седьмой сезон, выходящий в мае, пройдет именно там. Стильные пальто Джоан Уотсон будут
				смотреться на Бейкер-стрит еще лучше.</p>

			<img src="../resources/imgs/serial_vedmak.jpg" alt="Геральт от Netflix">
			<h4>«Ведьмак»</h4>
			<p>Экранизация «как бы книг, но на самом деле игр» с экс-Суперменом Генри Кавиллом в роли Геральта — для многих
				самая громкая премьера года. Может, именно этот сериал прервет череду неудач в экранизации видеоигр, может, именно
				на ТВ и надо было изначально пытаться адаптировать наши любимые игры? Два сериала по Mortal Kombat очень
				неплохо смотрелись в свое время…</p>
			<p>По самому «Ведьмаку» нет пока ничего, кроме пары секунд Кавилла в образе. Я все еще считаю, что по типажу это
				крайне странный кастинг, но плюс в том, что звезда такого масштаба в титульной роли говорит о серьезных намерениях
				проекта. Генри хороший актер и, если сценаристы справятся, из него выйдет отличный Геральт.</p>

			<h4>«Чем мы заняты в тени»</h4>
			<p>Меня все еще выводит из себя «перевод» изящного оригинального названия WHAT WE DO IN THE SHADOWS как «Реальные
				упыри», под которым оригинальный фильм вышел у нас в 2014 году, поэтому я рад, что в случае сериала его сменили на
				практически дословный. Я был уверен, что локализаторы сохранят неудачный заголовок для того, чтобы подчеркнуть
				связь с оригинальной кинолентой.</p>
			<p>Уморительная новозеландская «мокументалка» про вампиров (и немного про оборотней), созданная силами Тайки Вайтити
				(«Тор: Рагнарек», «Охота на дикарей») и Джемейна Клемента (половины прекрасного музыкально-комедийного дуэта и
				одноименного сериала «Полет Конкордов») решили превратить в сериал. Занимаются его созданием и пишут сценарий все
				те же Вайтити и Клемент, игравшие Вьяго и Влада соответственно. Но вот на экране в сериале они уже
