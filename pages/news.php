			<h4>Комиксы</h4>
			<p>Поток сериалов по комиксам и не думает иссякать, наоборот, намечаются громкие релизы. DC прорвало на новые
				сериалы, Marvel дразнит аудиторию «Виженом и Алой ведьмой», возможным продолжением «Сорвиголовы» на стриминговой
				платформе Disney+ (о котором официально пока не сказано ни слова) и отдельными шоу о Локки и дуэте Сокола с Зимним
				Солдатом.</p>

			<p>3 сезон «Джессики Джонс» и2 сезон «Карателя», судя по всему, все-таки выйдут на Netflix в этом году. На этом эра
				супергеройских сериалов на платформе закончится.</p>

			<h4>«Хранители»</h4>
			<p>Сериал не станет еще одной экранизацией графического романа Алана Мура (Снайдер может и запорол
				киновселенную-другую, но его «Хранители» все еще остаются лучшей экранизацией комиксов), а расскажет свою историю.
				Учитывая, что ее созданием занимается сценарист Lost и «Прометея» Дэймон Линделоф, проект вызывает
				предвкушение и опасения одновременно.</p>

			<p>Роль Джереми Айронса в сериале недавно была раскрыта — он сыграет Озимандиса, которого у Снайдера шикарно раскрыл
				Мэттью Гуд. Учитывая разницу в возрасте актеров, события оригинального романа явно далеко в прошлом, а значит и
				Роршаха мы, мягко говоря, вряд ли увидим. Без него, морального противовеса остальным героям, мир
				Хранителей будет совсем другим. Хотя в комиксах, например, вернули маску Роршаха на другом человеке…</p>

			<h4>«Убийственный класс»</h4>
			<p>Сериал, созданный братьями Руссо по одноименному комиксу. Это не Marvel и не DC, а внезапно Image Comics. Шоу
				посвящено школе ассасинов и выйдет уже 16 января на канале SyFy. Из заметных актеров — играющий жесткого
				преподавателя Бенедикт Вонг (помощник Доктора Стренджа), которого Руссо могли прихватить с собой из обширного
				актерского стойла Marvel.</p>

