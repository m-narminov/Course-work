				маловероятно.</p>
			<p>Место действия переносится в Нью-Йорк (Лоc-Анджелес отбросили потому, что там слишком солнечно, а Детройт —
				потому что там Джармуш уже селил своих вампиров в замечательном «Выживут только любовники»), а история фокусируется
				на новой группе вампиров-сожителей и их человеческом фамильяре. Сериал должен стартовать на канале FX уже весной.</p>

