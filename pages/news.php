			<h4>«Роковой патруль»</h4>
			<p>Так как собственные стриминговые сервисы с блэкджеком и оригинальными шоу сейчас — самый хайп, Warner Bros.
				запустили свою версию. Контента на ней пока — куда там Netflix. Целых две передачи, одна — ежедневное новостное
				шоу, другая — сериал «Титаны», выходящий по эпизоду в неделю. Но это только начало.</p>

			<p>В 2019-м на площадке появится аж пять новых шоу («Болотная тварь», речь о которой ниже — одно из них). Также
				ожидается сольное шоу про Старгерл, взрослый мультсериал, посвященный Харли Квин, продолжение «Юной Лиги
				Справедливости: Аутсайдеры» и, собственно, Роковой патруль.</p>

			<p>Режиссером шоу станет Глен Винтер, который работал оператором и постановщиком кучи супергеройских шоу, начиная с
				«Тайн Смолвиля» и заканчивая всей пачкой The CW и сочувствующих: «Легенды завтрашнего дня», «Супергерл», «Флэш» и
				«Стрела».</p>

			<p>Дум-патруль — это одна из первых супергеройских команд вселенной DC, появившаяся в 1963 году. Роботмэн,
				Негативмэн, Эласти-герл и Сумасшедшая Джейн были металюдьми-изгоями, которых собрал вместе человек в инвалидном
				кресле. Звучит знакомо? Да, «Люди-икс» по одной из версий были плагиатом с Рокового патруля. Теперь у оригинала
				есть шанс хотя бы немного сократить огромный разрыв в известности. Хотя, скорее всего, многие зрители решат,
				что DC плагиатит несчастных мутантов Marvel.</p>

			<p>Что выгодно отличает Патруль от Титанов, так это актерский состав: Мэтт Бомер, Тимоти Далтон, Брендан Фрейзер и
				вездесущий Алан Тьюдик.</p>

