			<h4>«Аватар: Повелитель стихий»</h4>
			<p>Впрочем, официального русского перевода еще нет, так что ориентируемся на оригинальное Avatar: The Last Airbender
				2019.</p>
			<p>Netflix в сотрудничестве с Nickelodeon (каналом, создавшим оригинальный сериал) попытается создать свою версию
				«Аватара» с живыми актерами. Занимаются этим создатели оригинального шоу и они очень, очень постараются не
				повторить ошибок Найта Шьямалана.</p>
			<p>«Легенда об Аанге» — это лучшее аниме, созданное не японцами. Мультсериал выходил с 2003 по 2008 годы, затем в
				2010-м Шьямалан обрушил на фанатов свою ужасную киноэкранизацию, где все разнообразные расы от эскимосов до азиатов
				выглядели подозрительно европеоидно. С 2012 по 2014 выходил спин-офф «Легенда о Корре», посвященный следующему
				после Аанга аватару, который, как я считаю, вышел еще круче оригинального шоу — одни только драки чего стоят,
				такого крутого анимационного экшена больше нигде и не встретишь, по крайней мере за пределами полнометражного
				аниме.</p>
			<p>Производство сериала только стартует в 2019-м, поэтому о шоу почти ничего не известно, но уже шанс того, что мы
				сможем получить нормальную версию «Аватара» с живыми актерами, не может не радовать.</p>

			<h4>«Эдди»</h4>
			<p>Дэмиен Шазелл, создатель «Одержимости», «Ла Ла Ленда» и Человека на луне», пробует себя в малом формате. Это
				восьмисерийный мюзикл о джаз-клубе в современном Париже для Netflix. Шазелл не только продюсирует шоу, но и снимает
				его целиком сам.</p>

			<h4>«Кровь и сокровища»</h4>
			<p>Продолжая тему талантливых кинорежиссеров на малом экране — Марк Уэбб («500 дней лета», «Новый Человек-паук»)
				продюсирует приключенческий сериал, где эксперт по антиквариату, вынужденный работать с воровкой произведений
				искусства, решает остановить криминального босса, финансирующего свои темные дела с помощью ворованных сокровищ.
				Кто сказал Uncharted?</p>

			<p>«Фосс/Вердон»(FOSSE/VERDON) — условное пока название минисериала канала FX, посвященного отношениям хореографа и
				режиссера Боба Фосса и танцовщицы Гвен Вердон. Оба — легенды Бродвея, чье сотрудничество навсегда изменило
				американскую индустрию развлечений. Почему это любопытно? Их играют Сэм Рокуэлл и Мишель Уильямс соответственно.</p>

