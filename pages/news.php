			<h4>«Властелин колец»</h4>
			<p>Amazon выкупила права за 250 миллионов долларов, заявлено 5 сезонов основного шоу и некий спин-офф. Один только
				основной сериал, который не станет прямой экранизацией книг, а расскажет о других событиях Средиземья, в итоге
				влетит площадке в миллиард долларов. Питера Джексона манят всяческими пирогами поучаствовать, но он не торопится
				возвращаться в мир, отожравший у него столько лет.</p>

			<h4>«Долгая ночь»</h4>
			<p>Спин-офф «Игры престолов» решает побить все рекорды в мире приквелов, отматывая время аж на восемь тысяч лет
				назад, к эпохе героев, к зиме, которая длилась целое поколение и принесла Белых Ходоков в мир Песни льда и огня.
				Дата релиза пока неизвестна и велик шанс, что она переползет на 2020-й.</p>

			<h4>«Страна Лавкрафта»</h4>
			<p>К сожалению, это не экранизация самого Лавкрафта, а лишь отсылка к нему. Шоу будет адаптацией одноименного романа
				Мэтта Раффа, в котором чернокожий парень с подругой и дядей отправляются в путешествие по Америке пятидесятых
				годов, сталкиваясь с одной стороны с расизмом эры законов Джима Кроу, с другой — с
				«лавкрафтианскими» монстрами. Шоуранером этого фэнтэзи-хоррора выступит Миша Грин, а исполнительными продюсерами —
				Джордан Пил и Джей Джей Абрамс.</p>

			<h4>«Благие намерения»</h4>
			<p>Роман Терри Пратчетта и Нила Геймана, вышедший в 1990 году, получил экранизацию — и какую! С Джоном Хэмом в
				образе архангела Гавриила и с Фрэнсис Макдорманд в роли Бога. Главные же роли — ангела и демона, пытающихся
				остановить апокалипсис — достались Дэвиду Тенанту и Майклу Шину.</p>

